package org.ngrinder.perftest.service;

/**
 * Console information which contains console's characteristics. Port is most
 * important one.
 * 
 * @author JunHo Yoon
 * @since 3.0
 * 
 */
public class ConsoleEntry {

	/**
	 * Console port number.
	 */
	private Integer port;

	/**
	 * Constructor.
	 * 
	 * @param port
	 *            port
	 */
	public ConsoleEntry(Integer port) {
		this.port = port;
	}

	/**
	 * Get port
	 * 
	 * @return port
	 */
	public Integer getPort() {
		return port;
	}

	/**
	 * Set port
	 * 
	 * @param port
	 *            port number
	 */
	public void setPort(Integer port) {
		this.port = port;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((port == null) ? 0 : port.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		ConsoleEntry other = (ConsoleEntry) obj;
		if (port == null) {
			if (other.port != null) {
				return false;
			}
		} else if (!port.equals(other.port)) {
			return false;
		}
		return true;
	}

}
